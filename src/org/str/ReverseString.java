package org.str;

public class ReverseString {
	 
	public static void main(String args[]) {
		
		 String s =" Greens@1234";
		 String r ="";
		 
		 
		 
		for(int i=s.length()-1;i>0;i--)
		{
			char ch = s.charAt(i);
			r=r+ch;
			System.out.print(ch);
		}
		
	}
	
	
	
		 
	 }
	 
	 
	 
	 
	 

	 
	
	 
	 
	 
	 






